# [problem]
# 혈액형(A, B, O, AB)을 입력받아 두 줄로 출력하시오.
# 혈액형: {혈액형}형
# 성격: {설명}

# 성격 기준:
# A형: 꼼꼼하고 신중한 성격
# B형: 자유롭고 창의적인 성격
# O형: 사교적이고 리더십이 강한 성격
# AB형: 이성적이고 독창적인 성격
# 그 외 입력은 잘못된 입력입니다.만 출력
# 주의: AB가 A보다 먼저 검사되어야 합니다.

# 예시:
# 입력: AB → 출력: 혈액형: AB형\n성격: 이성적이고 독창적인 성격
# 입력: A → 출력: 혈액형: A형\n성격: 꼼꼼하고 신중한 성격
# 입력: X → 출력: 잘못된 입력입니다.


# 사용자로부터 혈액형을 입력받기
blood_type = input()

# 입력 받은 혈액형에 따라 판별
if blood_type == "AB":
    # AB형을 A보다 먼저 검사해야 합니다.
    # AB형일 경우 "혈액형: AB형\n성격: 이성적이고 독창적인 성격"을 저장
    personality = "혈액형: AB형\n성격: 이성적이고 독창적인 성격"

elif blood_type == "A":
    # A형일 경우 "혈액형: A형\n성격: 꼼꼼하고 신중한 성격"을 저장
    personality = "혈액형: A형\n성격: 꼼꼼하고 신중한 성격"

elif blood_type == "B":   
    # B형일 경우 "혈액형: B형\n성격: 자유롭고 창의적인 성격"을 저장
    personality = "혈액형: B형\n성격: 자유롭고 창의적인 성격"

elif blood_type == "O":     
    # O형일 경우 "혈액형: O형\n성격: 사교적이고 리더쉽이 강한 성격"을 저장
    personality = "혈액형: O형\n성격: 사교적이고 리더쉽이 강한 성격"

else:    
    # 그 외 입력일 경우 "잘못된 입력입니다."를 저장
    personality = "잘못된 입력입니다."
    
# 최종 결과 출력
print(personality)