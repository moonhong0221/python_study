# [problem]
# 배터리 잔량(%)을 입력받아 20 이하이면 충전 필요, 
# 아니면 배터리 충분을 출력하시오.

# 예시:
# 입력: 15 → 출력: 충전 필요
# 입력: 80 → 출력: 배터리 충분
# 입력: 20 → 출력: 충전 필요


# 사용자로부터 입력값을 받기
battery = int(input())

# 입력값이 20이하인지 아닌지 판별
if battery <= 20:
    # 20이하이면 "충전 필요"를 출력
    print("충전 필요")
    
else:    
    # 아니라면 "배터리 충분"을 출력
    print("배터리 충분")