# 상품 재고 수량을 입력받아 아래 규칙에 따라 출력하시오.
# 재고가 0이면 두 줄 출력: 품절입니다, 입고 예정입니다
# 재고가 0이 아니면 한 줄 출력: 구매 가능합니다
# 어떤 경우든 마지막에는 쇼핑을 계속합니다를 출력

# 예시:
# 입력: 0 → 출력: 품절입니다\n입고 예정입니다\n쇼핑을 계속합니다
# 입력: 5 → 출력: 구매 가능합니다\n쇼핑을 계속합니다


# 사용자로부터 상품 재고 수량을 입력받기
stock = int(input())

# 상품재고 수량이 있는지 없는지 판별
if stock == 0:
    # 재고가 없으면 "품절입니다", "입고 예정입니다"를 출력 (마지막에는 항상 "쇼핑을 계속합니다"를 출력)
    print("품절입니다\n입고 예정입니다\n쇼핑을 계속합니다")

else:    
    # 재고가 있으면 "구매 가능합니다"를 출력 (마지막에는 항상 "쇼핑을 계속합니다"를 출력)
    print("구매 가능합니다\n쇼핑을 계속합니다")