# [problem]
# 금액(원)을 입력받아 500원, 100원, 50원, 10원 동전으로 최소 개수 변환하여 다음 형식으로 출력하시오.
# {금액}원 → 동전 변환:
# 500원: {n1}개
# 100원: {n2}개
# 50원: {n3}개
# 10원: {n4}개
# 총 동전 수: {합계}개
# 10원 미만은 버립니다. 각 동전 수가 0이어도 해당 줄은 모두 출력합니다.

# 예시:
# 입력: 1730 → 출력: 1730원 → 동전 변환:\n500원: 3개\n100원: 2개\n50원: 0개\n10원: 3개\n총 동전 수: 8개


# 사용자로부터 입력값을 입력받기
amount = int(input())

# 각 동전별 갯수 초기값
n1 = n2 = n3 = n4 = 0

# 입력값 따로 저장
money = amount

# 입력 받은 금액을 500, 100, 50, 10원으로 나누어 저장
if amount >= 500:   # 500원은 500으로 나누기
    
    # 500원 동전의 갯수
    n1 = amount // 500

    # 500원으로 나누어 지지 않는 나머지 값
    amount = amount % 500

if amount >= 100:   # 100원은 100으로 나누기
    
    # 100원 동전의 갯수
    n2 = amount // 100

    # 100원으로 나누어 지지 않는 나머지 값
    amount = amount % 100

if amount >= 50:   # 50원은 50으로 나누기
    
    # 50원 동전의 갯수
    n3 = amount // 50

    # 50원으로 나누어 지지 않는 나머지 값
    amount = amount % 50

if amount >= 10:   # 10원은 10으로 나누기
    
    # 10원 동전의 갯수
    n4 = amount // 10

    # 10원으로 나누어 지지 않는 나머지 값
    amount = amount % 10

# 총 동전 수 계산
total = n1 + n2 + n3 + n4

# 최종 결과 출력
print(f"{money}원 → 동전 변환:\n500원: {n1}개\n100원: {n2}개\n50원: {n3}개\n10원: {n4}개\n총 동전 수: {total}개")