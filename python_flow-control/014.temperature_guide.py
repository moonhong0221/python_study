# 기온(정수)을 입력받아 아래 규칙에 따라 출력하시오.
# 기온이 0 미만이면 두 줄 출력: 영하입니다, 빙판길을 조심하세요
# 기온이 0 이상이면 한 줄 출력: 영상입니다
# 어떤 경우든 마지막에는 외출 준비 완료!를 출력

# 예시:
# 입력: -5 → 출력: 영하입니다\n빙판길을 조심하세요\n외출 준비 완료!
# 입력: 12 → 출력: 영상입니다\n외출 준비 완료!
# 입력: 0 → 출력: 영상입니다\n외출 준비 완료!


# 사용자로부터 기온을 입력받기
current_temp = int(input())

# 입력받은 기온이 0이상인지 미만인지 판별
if current_temp < 0:
    # 0미만이면 "영하입니다", "빙판길을 조심하세요"를 출력
    print("영하입니다\n빙판길을 조심하세요")
    
else:    
    # 0이상이면 "영상입니다"를 출력
    print("영상입니다")
    
# 어떤 경우든지 마지막에는 "외출 준비완료!"를 출력  
print("외출 준비완료!")  